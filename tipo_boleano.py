"""
Tipo Booleano

Algebra Booleana, criada por George Boole

2 constantes, verdadeira ou falsa

True = verdadeiro
False = falso
"""
logado = False
ativo = True
print(ativo)

"""
OPerções básicas
"""

# Negação
print(not ativo)

# Ou, é uma operação binária.
print(ativo or logado)

# E, para passar as duas opções devem ser verdadeiras

print(ativo and logado)

print (ativo and not logado)